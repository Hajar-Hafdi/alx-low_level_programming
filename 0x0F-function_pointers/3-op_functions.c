#include "3-calc.h"

/**
 * op_add - adds two integers
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substractes 2 ints
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies 2 ints
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: result of multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that executes a division
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo 2 integers
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: remainder of int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
